package wv.kmg.mapprinter;

public class Triangle {
	double[][] source;
	double[][] target;
	public Triangle(double[][] source, double[][] target) {
		super();
		this.source = source;
		this.target = target;
	}
	public double[][] getSource() {
		return source;
	}
	public double[][] getTarget() {
		return target;
	}
	
}
